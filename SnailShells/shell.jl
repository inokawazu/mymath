md"""
Luckily real looking shells are not that far behind. First we can generalized the spiral we had before to be $\left<R(\theta)*\cos(\theta), R(\theta)*\sin(\theta), h(\theta)\right>$. Now the height and the Radius (from the z-axis) depend on $\theta$.
I left the Julia guts exposed again for those intereseted.
"""

# ╔═╡ 2bb0d524-0714-48fb-bb5c-e5a851cb76a7
function spiral(θ, R::Function, h::Function)
	[R(θ)*cos(θ), R(θ)*sin(θ), h(θ)]
end

# ╔═╡ edc01f03-ecdb-4c94-98b5-233cf69a6364
let sp = θ′ -> spiral(θ′; R = 1, slope = 0.5)
	spp = hcat(sp.(range(0, 3pi, length=20))...)
	plot3d(eachrow(spp)..., label=nothing)
	title!("Sprial with linear slope 1 and constant radius 1")
end

# ╔═╡ 1a947bb4-8c2a-4c95-a6b6-27ab2dcd4572
function tube_spiral(θ, ϕ; R = 1.0, r = 0.5, slope = 1.0)
	norml(x) = x/√(sum(x.^2))
	
	sp(θ′) = spiral(θ′; R=R, slope=slope)
	v̂(θ′) = (norml∘der)(sp, θ′)
	â = (norml∘der)(v̂, θ)
	p̂ = v̂(θ) × â
	sp(θ) + r*â*cos(ϕ) + r*p̂*sin(ϕ)
end

# ╔═╡ bf70c582-ca34-483e-b979-d3e473c833ab
function plot_tube_spiral(;R = 1.0, r = 1.5, slope = 1.0,n = 10) 
	tsp = (θ,ϕ) -> tube_spiral(θ, ϕ; R = R, r = r, slope = slope)
	
	tspp = [tsp(θ′,ϕ′) for θ′ in range(0, 4pi, length=n) for ϕ′ in range(0, 2pi, length=n)]
	tssppp = eachrow(hcat(tspp...))
	scatter3d(tssppp..., markersize=0.5)
	title!("Tube sprial with R=$R, slope=$slope, r=$r")
end

