md"""
The goal is two specify a function that takes locations on a 2D grid and spits our locations onto a 3D space (a map f:$\mathbb{R}^2\rightarrow\mathbb{R}^3$ for my mathematical buddies).
On can think of this function a labeling all the points on our to shell.
I can think of one sort of label/coordinate.
On can say as we go up the spiral, that's labeled by θ the angle!
The other angle, let`s say ϕ determines the angle about a point about the "spine" of our shell.
Now actually the function f(ϕ, θ) is another matter.
It's really all in the details. 
Those detail will be some!
In the meantime, I guess meaninful values of ϕ would be from 0 to 2π while θ can generically be any number from -∞ to ∞.
This is something for the reader to think about.
