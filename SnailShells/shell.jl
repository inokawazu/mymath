# ╔═╡ 1eb23e3a-689b-41fd-ba71-cdd119c65969
md"""
Now for the real math!
First, realize that we can work with our previous curve as a scaffolding so to speak.
All we have to do is specify a value R that is the radius of the shell.
This radius will be perpedicualar around the spine at every point (and value of θ).

So really this is quite straightforward, we just need two vectors of length one along the spine that are mutually perpedicular to eachother and the direction of the spine. Let's call these vector $\hat{a}$ and $\hat{p}$.
For your day in calculus we remember that a curve that is parmeterized can also have a tangential vector calculated from it.
It's the unit vector of the derivative of that curve!
Let's say locations on the spine are located by ($\vec{S}(\theta)=\left<R \sin(\theta), R \cos(\theta),b \theta \right>$). Remember this guy?

Before we go further, I would like to just point out that the function for this "shell" is just the following, $f(θ,ϕ)=\vec{S}(\theta) + R\cos(\phi)\hat{a} + R\sin(\phi)\hat{p}$.
Why well, the answer is that the $\vec{S}$ takes us from the origin to the spine and the $R\cos(\phi)\hat{a} + R\sin(\phi)\hat{p}$ bit takes use from the spine to the circle perpendicular to the "spine", ie on the shell!
Well, this is shap is more of a "tube" shell. 
Look below at the picture! 

A bit more math. 
One may be wondering how and what are $\hat{a}$ and $\hat{p}$ calculated?
First off, $\hat{a}$ is actually just the direction of the velocity of the unit tangent vector. 
To find that we first find $\dot{\vec{S}}(\theta)\equiv d\vec{S}(\theta)/d\theta$.
The velocity of the unit tangent vector is fancy talk for $\vec{a}=\frac{d}{d\theta}{\frac{\dot{\vec{S}}}{\sqrt{\dot{\vec{S}}\cdot\dot{\vec{S}}}}}$. Notice that's a vector, not a hat!
For $\hat{p}$ it's very simple. It's just $\hat{p} = \frac{\dot{\vec{S}}\times\hat{a}}{\sqrt{\dot{\vec{S}}\cdot\dot{\vec{S}}}}$
"""

