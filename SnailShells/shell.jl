# ╔═╡ 9bd5effc-f3c0-4896-b099-b065b08e594d
md"""
Now it's not too much work to make a more generic shell!
We can just take a sprial, for a given $R(\theta)$ and $h(\theta)$ and introduce a now variable minor radius around the spine, $r(\theta, \phi)$, (that notably depends on both $\theta$ and $\phi$). 
I will leave it to the reader to think why R and h don't depend on $\phi$. 
The new $f$ is $f(θ,ϕ)=\vec{S}(\theta; R,h) + r(\theta, \phi)\cos(\phi)\hat{a} + r(\theta, \phi)\sin(\phi)\hat{p}$.
$\vec{S}(\theta; R,h)$ now just means, the curve $S$ will change for different $R$ and $h$.
"""

# ╔═╡ a8701496-a525-4aea-b9b6-a6e8e9180b40
function spiralshell(θ, ϕ, R::Function, h::Function, r::Function)
	norml(x) = x/√(sum(x.^2))
	
	sp(θ′) = spiral(θ′, R, h)
	v̂(θ′) = (norml∘der)(sp, θ′)
	â = (norml∘der)(v̂, θ)
	p̂ = v̂(θ) × â
	sp(θ) + r(θ, ϕ)*(â .* cos(ϕ) + p̂ .* sin(ϕ))
end

# ╔═╡ 8fdf4d50-fa78-450b-b1ff-6611867dc00a
function plot_spiralshell(; R, h, r, 
	delta = 0.5, θmax = 20pi, θmin=0, legend = nothing, color=:tol_bright, showaxis=false, plotopts...)

	sps = (θ′,ϕ′) -> spiralshell(θ′,ϕ′, R, h, r)
	sp = θ′ -> spiral(θ′, R, h)
	spsp = [sps(θ′,ϕ′) for θ′ in range(θmin, θmax, step=delta), ϕ′ in range(0, 2pi, step=delta)]
	x = getindex.(spsp,1)
	y = getindex.(spsp,2)
	z = getindex.(spsp,3)
	surface(x,y,z;legend = legend, color=color, showaxis=showaxis, plotopts...)
end

