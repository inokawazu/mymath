# ╔═╡ 509466eb-e4bb-4357-ade5-d72b111d84ef
md"""
Let's start simple. 
One might notice that shells have a "spiral" shape to them. 
So maybe if we understand how to represent a spiral (line), it will be an easier step to representing a shell mathematically (as a surface). 
One way to represent a spiral at least is with a function (the function is called spiral below)! 
The spiral takes some anlge $\theta$ and spits out a location on a cartesian grid as $\left<R \sin(\theta), R \cos(\theta),b \theta \right>$ that a poisition the spiral occupies.
Let's take a look it!
"""

# ╔═╡ 7f9bd9bc-25f4-42b0-9c95-b7e29d2d11c6
spiral(θ; R = 1, slope = 0) = [R*cos(θ), R*sin(θ), slope*θ]

